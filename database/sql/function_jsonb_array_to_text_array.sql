-- DROP FUNCTION IF EXISTS public.jsonb_array_to_text_array(jsonb);
CREATE OR REPLACE FUNCTION PUBLIC.JSONB_ARRAY_TO_TEXT_ARRAY(_JS jsonb)
  RETURNS text[]
  LANGUAGE 'sql'
  COST 100 IMMUTABLE STRICT PARALLEL SAFE RETURN ARRAY(
    SELECT JSONB_ARRAY_ELEMENTS_TEXT(_JS) AS JSONB_ARRAY_ELEMENTS_TEXT
);

